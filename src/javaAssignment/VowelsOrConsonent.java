public class VowelsOrConsonent {
	//Vowels with if else Statement
	public static void main(String[] args) {
		char c='U';
		if((c =='a')||(c=='A')){
			System.out.println("Given character is Vowel : " +c);
		}
		else if((c=='e')||(c=='E')) {
			System.out.println("Given character is Vowel : " +c);
		}
		else if((c=='i')||(c=='I')) {
			System.out.println("Given character is Vowel : " +c);
		}
		else if((c=='o')||(c=='O')) {
			System.out.println("Given character is Vowel : " +c);
		}
		else if((c=='u')||(c=='U')) {
			System.out.println("Given character is Vowel : " +c);
		}
		else {
			System.out.println("Given character is Consonent : " +c);
		}
		
	}

}
